#include "./libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*s;

	s = "0123456789";
	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd(s[n % 10], fd);
}
