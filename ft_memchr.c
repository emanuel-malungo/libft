#include "./libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	int					i;
	unsigned const char	*str;

	i = 0;
	str = (unsigned const char *)s;
	while (i < n)
	{
		if (str[i] == (unsigned char)c)
			return ((void *)&str[i]);
		i++;
	}
	return (NULL);
}
